// ○ 문제 분석
//	  공백이 먼저 출력된 후 *이 출력되도록 구현
//	  행 단위의 연산이 끝나면 개행하고 이를 5번(열) 반복하도록 한다.


public class Test063
{
	public static void main(String[] args)
	{
		int lin, row;									// 루프 변수 선언

		for (row=1; row<=5; row++)						// 열을 쌓는 반복 연산
		{	
			// 행을 만드는 연산
			for (lin=1; lin<row; lin++)					// 공백을 만드는 반복 연산
				System.out.print(" ");
			for (lin=1; lin<=9-(2*row-2); lin++)		// *을 만드는 반복 연산
				System.out.print("*");

			System.out.println();						// 개행
		}
	}
}

